    private void btnUangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUangActionPerformed
        double pembayaran = Double.parseDouble(txtUang.getText());
        double bayar = Double.parseDouble(txtBayar.getText());
        
        if(bayar > pembayaran ){
            double uk = bayar - pembayaran;
            JOptionPane.showMessageDialog(this, "sebanyak : "+uk,"uang anda kurang",JOptionPane.INFORMATION_MESSAGE);
        }else if (pembayaran > bayar){
            double ul = pembayaran - bayar;
            JOptionPane.showMessageDialog(this, "sebanyak :"+ul,"uang anda berlebih",JOptionPane.INFORMATION_MESSAGE );
        }
       
    }//GEN-LAST:event_btnUangActionPerformed

