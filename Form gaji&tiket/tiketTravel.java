    private void btnHargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHargaActionPerformed
        int kelas = (int)cbKelas.getSelectedIndex();
        if (!rbBkt.isSelected() && !rbSlk.isSelected()&&!rbPar.isSelected()){
            JOptionPane.showMessageDialog(this, "pilih dulu baru berangkat","pesan kesalahan",JOptionPane.ERROR_MESSAGE);
        }else if (cbKelas.getSelectedIndex()==0){
         JOptionPane.showMessageDialog(this, "kelas belum dipilih","Pesan kesalahan",JOptionPane.ERROR_MESSAGE);
        }
            
        switch(kelas){
            case 1: if(rbBkt.isSelected())
                txtTiket.setText("25000");
            else if(rbSlk.isSelected())
                txtTiket.setText("20000");
            else if(rbPar.isSelected())
                txtTiket.setText("15000");
            break;
            
            case 2: if(rbBkt.isSelected())
                txtTiket.setText("50000");
            else if(rbSlk.isSelected())
                txtTiket.setText("40000");
            else if (rbPar.isSelected())
                txtTiket.setText("30000");
            break;
            
            case 3: if (rbBkt.isSelected())
                    txtTiket.setText("80000");
            else if (rbSlk.isSelected())
                txtTiket.setText("70000");
            else if (rbPar.isSelected())
                txtTiket.setText("55000");
            break;
                    
        }
    }//GEN-LAST:event_btnHargaActionPerformed

