    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        try{
        
        int jumlah = Integer.parseInt(txtJumlah.getText());
        if (jumlah <= 0){
            JOptionPane.showMessageDialog(this, "jumlah tiket tidak boleh 0","kalau 0 mau berangkat pakai apa?", JOptionPane.ERROR_MESSAGE);
        }
        double harga = Double.parseDouble(txtTiket.getText());
        double total = jumlah * harga;
        double diskon = 0;
        if (jumlah > 5 ){
            diskon = 0.1*total;
            double bayar = total-diskon;
            
            txtTotal.setText(Double.toString(total));
            txtDiskon.setText(Double.toString(diskon));
            txtBayar.setText(Double.toString(bayar));
        
        }else{
            double bayar = total;
            txtTotal.setText(Double.toString(total));
            txtDiskon.setText(Double.toString(diskon));
            txtBayar.setText(Double.toString(bayar));
        }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "error","jumlah harus angka dan bil genap",JOptionPane.ERROR_MESSAGE);
            txtJumlah.setText("");
            txtJumlah.requestFocus();
        }
                    
                    
    }//GEN-LAST:event_btnHitungActionPerformed

